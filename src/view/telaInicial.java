    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        ContaDAO dao = new ContaDAO();
        Conta c = new Conta();
        
        c.setLogin(txtLogin.getText());
        c.setSenha(Criptografia.criptografar(txtSenha.getText()));
        
        contaEncontrada = dao.buscarConta(c);
        
        if(contaEncontrada != null){
            telaPrincipal telaP = new telaPrincipal();
            telaP.nomeExibicao(contaEncontrada.getNome());
            telaP.mostrarPontos(contaEncontrada.getPontos());
            dispose();
            telaP.setVisible(true);
            nomecliente = contaEncontrada.getNome();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Conta n√£o encontrada!");
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

